
public class Funcionario {
	private String funcao;
	private Cadastro funcionario;
	
	public Funcionario() {
		this.funcao = funcao;
		this.funcionario = funcionario;
	}

	public String getFuncao() {
		return funcao;
	}

	public void setFuncao(String funcao) {
		this.funcao = funcao;
	}

	public Cadastro getFuncionario() {
		return funcionario;
	}

	public void setFuncionario(Cadastro funcionario) {
		this.funcionario = funcionario;
	}

}
