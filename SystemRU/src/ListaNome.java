
public class ListaNome {
	private String nomeDieta;
	private String nomeVegetariano;
	
	public ListaNome() {
		this.nomeDieta = nomeDieta;
		this.nomeVegetariano = nomeVegetariano;
		
	}

	public String getNomeDieta() {
		return nomeDieta;
	}

	public void setNomeDieta(String nomeDieta) {
		this.nomeDieta = nomeDieta;
	}

	public String getNomeVegetariano() {
		return nomeVegetariano;
	}

	public void setNomeVegetariano(String nomeVegetariano) {
		this.nomeVegetariano = nomeVegetariano;
	}

}
