
public class Cardapio {
	public String data;
	public String refeicao;
	public String pratoPrincipal;
	public String dieta;
	public String vegetariano;
	public String guarnicao;
	public String sobremesa;
	public String suco;

	public Cardapio() {
		this.data = data;
		this.refeicao = refeicao;
		this.pratoPrincipal = pratoPrincipal;
		this.dieta = dieta;
		this.vegetariano = vegetariano;
		this.guarnicao = guarnicao;
		this.sobremesa = sobremesa;
		this.suco = suco;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getRefeicao() {
		return refeicao;
	}

	public void setRefeicao(String refeicao) {
		this.refeicao = refeicao;
	}

	public String getPratoPrincipal() {
		return pratoPrincipal;
	}

	public void setPratoPrincipal(String pratoPrincipal) {
		this.pratoPrincipal = pratoPrincipal;
	}

	public String getDieta() {
		return dieta;
	}

	public void setDieta(String dieta) {
		this.dieta = dieta;
	}

	public String getVegetariano() {
		return vegetariano;
	}

	public void setVegetariano(String vegetariano) {
		this.vegetariano = vegetariano;
	}

	public String getGuarnicao() {
		return guarnicao;
	}

	public void setGuarnicao(String guarnicao) {
		this.guarnicao = guarnicao;
	}

	public String getSobremesa() {
		return sobremesa;
	}

	public void setSobremesa(String sobremesa) {
		this.sobremesa = sobremesa;
	}

	public String getSuco() {
		return suco;
	}

	public void setSuco(String suco) {
		this.suco = suco;
	}

}
